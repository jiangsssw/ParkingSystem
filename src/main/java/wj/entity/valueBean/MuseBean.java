package wj.entity.valueBean;

public class MuseBean {
    private int museId;
    private int role;
    private String museName ;
    private String museUrl;

    public int getMuseId() {
        return museId;
    }

    public void setMuseId(int museId) {
        this.museId = museId;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getMuseName() {
        return museName;
    }

    public void setMuseName(String museName) {
        this.museName = museName;
    }

    public String getMuseUrl() {
        return museUrl;
    }

    public void setMuseUrl(String museUrl) {
        this.museUrl = museUrl;
    }
}
